    SUBROUTINE PrimarySourcetoField(cSpace)

    ! =============================================================================
    !
    !   Programmer: Koos Huijssen / Jasper de Koning
    !
    !   Language: Fortran 90
    !
    !   Version Date    Comment
    !   ------- -----   -------
    !   1.0     090505  Original code (KH)
    !
    ! *****************************************************************************
    !
    !   DESCRIPTION
    !
    !   The subroutine PrimarySourcetoField computes the primary field on the given
    !   space that is generated by the primary source.
    !
    ! *****************************************************************************
    !
    !   INPUT/OUTPUT PARAMETERS
    !
    !   cSpace   io   type(space)  Space structure for which the primary field
    !                              solution is to be obtained
    !
    type(Space), intent(inout)::                cSpace;

    ! *****************************************************************************
    !
    !   LOCAL PARAMETERS
    !
    !   cSource         type(Space)   Space containing the primary source
    !   iMarginStartZ   i8b           Start margin in Z for d/dz operation
    !   iMarginStartT   i8b           Start margin in T for d/dz operation
    !   iSourceDimT     i8b           Dimension of the source in T
    !   iSourceDimX     i8b           Dimension of the source in X
    !   iSourceDimY     i8b           Dimension of the source in Y
    !   cSpaceMargin    type(Space)   Space for the field with margins for d/dz
    !   cSpaceXYZMargin type(Space)   Space for the field with margins in XYZ only
    !   aiMargin        i8b           Margins in all four dimensions T/X/Y/Z
    !   dTempPhaseDelay dp            Maximum phase delay in the source
    !   acTemp          char          Temporary char array for output log messages
    !
    type(Space) ::				cSource
    integer(i8b)::              iMarginStartZ, iMarginStartT , i
    integer(i8b) ::				iSourceDimT,iSourceDimX,iSourceDimY
    type(Space)::               cSpaceMargin,cSpaceXYZMargin
    integer(i8b) ::             aiMargin(4)
    real(dp) ::					dTempPhaseDelay
    character(len=1024)::       acTemp
	
	!Filtering and windowing parameters 
    integer(i8b)		::	iDimW,iStart, iIndex
    real(dp) 			::	dDOmega, dFFTFactor
    real(dp), allocatable 	::	dTaperSupportWindow(:), dTaperMaxFreqWindow(:), dMultFactor(:)
    real(dp) 			::	dLeftBand,dRightBand,dCorrection
    
    ! *****************************************************************************
    !
    !   I/O
    !
    !   log file entries
    !
    ! *****************************************************************************
    !
    !   SUBROUTINES/FUNCTIONS CALLED
    !
    !   SwStartAndCount
    !   PrintToLog
    !   iBeamOffsetT
    !   iBeamOffsetX
    !   iBeamOffsetY
    !   SourceDimensions
    !   InitSpace
    !   InitGrid
    !   SpaceInfo
    !   InitSource
    !   ExportSlice
    !   ExportArray
    !   ConvolutionGreen
    !   DestructSpace
    !   BeamDerivativeZ
    !   GridDistr0CreateEmpty
    !   CopyDataD1
    !   CopyDataD2
    !   ReorderDistr0ToDistr1
    !   ReorderDistr1ToDistr2
    !   ReorderDistr2ToDistr1
    !   ReorderDistr1ToDistr0
    !   GridDistr2CreateEmpty
    !   SWStop
    !
    ! *****************************************************************************
    !
    !   PSEUDOCODE
    !
    !   If the source is an F-source and the aperture is not a point source
    !       Compute the margins in all directions needed for the d/dz operator
    !   Obtain the dimensions in all directions of the source
    !   Initialize a field space including possible margins
    !   Initialize a source space with the primary source definition
    !   Export the source space
    !   Compute the convolution of the source with the Green's function to obtain
    !     the primary field in the field space
    !   Destruct the source space
    !   If the source is an F-source and the aperture is not a point source
    !       Apply the d/dz operator to the field space
    !   Reduce the field space with margins to a field space without margins
    !
    ! =============================================================================


    call SwStartAndCount(cswLinStep);
    call PrintToLog("PrimarySourcetoField",1)

    ! 1) Compute the necessary margin (in all directions) to compute the linear step
    !KH not only an extra margin in the z-direction, also in t,x and/or y if the theta's are /= pi/2!!
    !KH this is only necessary if dp/dz needs to be determined, i.e. in case of an F source
    if (cSourceParams%srcshapetype/=iHI_POINTSOURCE.AND.cModelParams.PrimarySourceType==iEI_FSOURCE) then
        aiMargin(1) = ceiling(abs(cModelParams%FDXOrder/2 *cSpace%dTanT) - 1e-5)
        aiMargin(2) = ceiling(abs(cModelParams%FDXOrder/2 *cSpace%dTanX) - 1e-5)
        aiMargin(3) = ceiling(abs(cModelParams%FDXOrder/2 *cSpace%dTanY) - 1e-5)
        aiMargin(4) = cModelParams%FDXOrder/2;
    else
        aiMargin= (/ 0,0,0,0 /)
    end if

    !New global start positions in Z and T
    iMarginStartZ = cSpace%iStartZ-aiMargin(4)
    iMarginStartT = cSpace%iStartT+iBeamOffsetT(cSpace,-aiMargin(4))-aiMargin(1)

    ! Determine source dimensions: determined by either the dim+margin of
    ! the field or the minimum size required for the source+margin.
    call SourceDimensions(iSourceDimT,iSourceDimX,iSourceDimY,cSpace%dDt,cSpace%dDx)
    ! The dimension of the T-axis should be equal for the source and field
    ! space. Take the field space size for both.
    iSourceDimT = cSpace%iDimT+2*aiMargin(1)

    ! 2) Create a space (cSpaceMarg) with the extra margin in all txyz-directions
    call InitSpace(cSpaceMargin, iSI_FIELD, cModelParams.UseYSymmetry, &
        iSourceDimT,cSpace%iDimX+2*aiMargin(2), cSpace%iDimY+2*aiMargin(3), cSpace%iDimZ+2*aiMargin(4), &
        iMarginStartT,0_i8b,0_i8b,iMarginStartZ, 0_i8b,0_i8b,0_i8b, &
        cSpace%dFnyq, cSpace%dTanX, cSpace%dTanY, cSpace%dTanT)
    call InitGrid(cSpaceMargin, cSpace%cGrid%iProcN, cSpace%cGrid%iProcID, (/ .true., .true., .true., .true./));

    ! Correct the aiMargins for X and Y, may be changed because of resetting array size in InitGrid
    ! NB This only gives the correct value for the Start margin, which may or may not be the
    ! same as the End margin...
    ! (however, the start margin is the only one that is needed in CopyDataD1/2 anyway...)
    ! the correction needs to be done for the X and Y dimensions only, for the T and Z dimensions,
    ! the array size resetting only gives extra points at the end, start margin is unaffected
    aiMargin(2) = cSpace%iStartX - (cSpaceMargin%iStartX + iBeamOffsetX(cSpaceMargin,aiMargin(4)));
    aiMargin(3) = cSpace%iStartY - (cSpaceMargin%iStartY + iBeamOffsetY(cSpaceMargin,aiMargin(4)));

!     call PrintToLog("Variables in cSpaceMargin",-1)
!     call SpaceInfo(cSpaceMargin)

    write(acTemp, '("aiMargin in T, X, Y, Z: ", 4I2)') aiMargin
    call PrintToLog(acTemp,2)

    ! 2b) Initialize the source
    call InitSpace(cSource, iSI_PRIMARYSOURCE, .false., &
        iSourceDimT,iSourceDimX, iSourceDimY, 1_i8b, &
        -aiMargin(1),0_i8b,0_i8b,0_i8b,0_i8b,0_i8b,0_i8b, &
        cSpaceMargin%dFnyq, cSpaceMargin%dTanX, cSpaceMargin%dTanY, cSpaceMargin%dTanT)
    call InitGrid(cSource, cSpaceMargin%cGrid%iProcN, cSpaceMargin%cGrid%iProcID, (/ .true., .true., .true., .true./));
    call GridDistr0CreateEmpty(cSource%cGrid);

    call InitSource(cSource);	! Initialize the source as defined in the config file

    ! Export the source, only if nearfield is obtained
    if(cSpace%iStartZ==nint(cModelParams.Sz/cSpace%dDx)) then
        call ExportSlice(        trim(sOutputDir) // trim(sOutputRoot) // "src",&
            "Source", &
            cSource, &
            (/ -cSource%iStartT, 0_i8b, 0_i8b, 0_i8b /), &
            (/ cSpace%iDimT, cSource%iDimX, cSource%iDimY, 1_i8b /), &
            cModelParams.xyzsliceindex, cSource%iDimZ, .true.);
        if(cSpace%cGrid%iProcID==0) then
            dTempPhaseDelay = cSourceParams.maxphasedelay / cModelParams%freq0
            call ExportArray(trim(sOutputDir) // trim(sOutputRoot) // "srcdelay",&
                "phasedelay", (/ dTempPhaseDelay /), cSource%cGrid%iProcID)
        end if
    end if

    ! 3) Compute the convolution on cSpaceMarg
    call PrintToLog('Compute the convolution in the linear step',2);
    call ConvolutionGreen(cSource, cSpaceMargin, .false.);
    call DestructSpace(cSource)

    ! 4) Compute the Finite Difference operator on cSpaceMarg
    ! Only for an F-source excitation type and if the source is not a point source
    if (cSourceParams%srcshapetype/=iHI_POINTSOURCE.AND.cModelParams.PrimarySourceType==iEI_FSOURCE) then
        call PrintToLog('Compute the Finite Difference in z on the field', 2);
        call BeamDerivativeZ(cSpaceMargin)
    end if

    ! 5a) Strip the d/dz margins in T
    !REMARK: this step is not necessary if there are no margins, i.e. if d/dz was not obtained...
    !		 it would save some time if in that case we simply copy the content of cSpaceXYZMargin to CSpace
    call PrintToLog('Store the results in a beam without margins', 2);
    call InitSpace(cSpaceXYZMargin, iSI_FIELD, cModelParams.UseYSymmetry, &
        cSpace%iDimT,cSpaceMargin%iDimX, cSpaceMargin%iDimY, cSpaceMargin%iDimZ, &
        iMarginStartT+aiMargin(1),0_i8b,0_i8b,iMarginStartZ,0_i8b,0_i8b,0_i8b, &
        cSpace%dFnyq, cSpace%dTanX, cSpace%dTanY, cSpace%dTanT)
    call InitGrid(cSpaceXYZMargin, cSpace%cGrid%iProcN, cSpace%cGrid%iProcID, (/ .false., .false., .false., .false./));
    call GridDistr0CreateEmpty(cSpaceXYZMargin.cGrid);
    call CopyDataD1(cSpaceMargin%cGrid, cSpaceXYZMargin.cGrid, aiMargin(1));
    call DestructSpace(cSpaceMargin);

!     call PrintToLog("Variables in cSpaceXYZMargin",-1)
!     call SpaceInfo(cSpaceXYZMargin)
    
    ! 5b) Strip the d/dz margins in XYZ
    !   - Reorder to distribution 2
    call ReorderDistr0ToDistr1(cSpaceXYZMargin%cGrid);
    call ReorderDistr1ToDistr2(cSpaceXYZMargin%cGrid);
    call GridDistr2CreateEmpty(cSpace%cGrid);

    !   - Strip
    call CopyDataD2(cSpaceXYZMargin%cGrid, cSpace%cGrid, aiMargin(2:4));

!     call PrintToLog("Variables in cSpace",-1)
!     call SpaceInfo(cSpace)
     
    !   - Revert to distribution 0
    call DestructSpace(cSpaceXYZMargin);
    call ReorderDistr2ToDistr1(cSpace%cGrid);
    call ReorderDistr1ToDistr0(cSpace%cGrid);

    cSpace%iSpaceIdentifier=iSI_FIELD;

    ! Deinitialize
    call SWStop(cswLinStep);

    END SUBROUTINE PrimarySourcetoField

    SUBROUTINE PlanewaveField(cSpace)

    ! =============================================================================
    !
    !   Programmer: Koos Huijssen
    !
    !   Language: Fortran 90
    !
    !   Version Date    Comment
    !   ------- -----   -------
    !   1.0     090505  Original code (KH)
    !
    ! *****************************************************************************
    !
    !   DESCRIPTION
    !
    !   The subroutine PlanewaveField computes the primary field solution in the
    !   form of a plane wave running through the space in the positive z-direction
    !
    ! *****************************************************************************
    !
    !   INPUT/OUTPUT PARAMETERS
    !
    !   cSpace   io   type(space)  Space structure for which the primary field
    !                              solution is to be obtained
    !
    type(Space), intent(inout)::                cSpace;

    ! *****************************************************************************
    !
    !   LOCAL PARAMETERS
    !
    !   cSource   type(Space)   Space containing the primary source - is
    !                       mainly used to get the signature right
    !   dZpos        dp     Position in z in wavelengths
    !   dDiffT       dp     Time difference between the source and field space
    !   dOmega       dp     Step size in omega
    !   dCorrection  dpc    Complex correction term to account for the time delay
    !   iLi          i8b    Loop counter over all xyz positions in cSpace
    !   iOmega       i8b    Loop counter over the angular frequency components
    !   acTemp       char   Temporary char array for output log messages
    !
    type(Space) ::				cSource
    character(len=1024)::       acTemp
    real(dp) ::					dZpos, dDiffT, dOmega
    complex(dpc) ::				dCorrection
    integer(i8b)::              iLi, iOmega
    integer(i8b) ::             iErr

    ! *****************************************************************************
    !
    !   I/O
    !
    !   log file entries
    !
    ! *****************************************************************************
    !
    !   SUBROUTINES/FUNCTIONS CALLED
    !
    !   SwStartAndCount
    !   PrintToLog
    !   GridDistr0CreateEmpty
    !   InitSpace
    !   InitGrid
    !   InitSource_Planewave
    !   ExportSlice
    !   ReorderDistr0ToDistr1
    !   MapVt
    !   TransformT
    !   GridDistr1CreateEmpty
    !   TransformTInv
    !   MapVtInv
    !   ReorderDistr1ToDistr0
    !   DestructSpace
    !   SWStop
    !
    ! *****************************************************************************
    !
    !   PSEUDOCODE
    !
    !   Initialize a source space with the primary source definition - primarily
    !     for the signature
    !   Export the source space
    !   Redistribute, Map in time and Fourier transform the source space in time
    !   For each xyz-position do
    !       Determine the time delay factor for the plane wave solution
    !       Obtain the field on this position as a delayed form of the source
    !         signature
    !   Destruct the source space
    !   Redistribute, Inverse map and inverse Fourier transform the source space in
    !     time
    !
    ! =============================================================================

    call SwStartAndCount(cswLinStep);
    call PrintToLog("PlanewaveField",1)

    ! 1) Obtain the source pulse
    ! We need one source trace on each processor.
    call InitSpace(cSource, iSI_PRIMARYSOURCE, .false., &
        cSpace%iDimT,cSpace%cGrid%iProcN, 1_i8b, 1_i8b, &
        0_i8b,0_i8b,0_i8b,0_i8b,0_i8b,0_i8b,0_i8b, &
        cSpace%dFnyq, cSpace%dTanX, cSpace%dTanY, cSpace%dTanT)
    ! The spatial dimensions have no physical meaning, but we need one time frame on each processor
    call InitGrid(cSource, cSpace%cGrid%iProcN, cSpace%cGrid%iProcID, (/ .false., .false., .false., .false./));
    call GridDistr0CreateEmpty(cSource%cGrid);
    call InitSource_Planewave(cSource);	! Initialize the source signature as defined in the config file
    ! Configuration is treated as though it is a point source

    if(cSpace%iStartZ==nint(cModelParams.Sz/cSpace%dDx)) then
        call ExportSlice(        trim(sOutputDir) // trim(sOutputRoot) // "src",&
            "Source", &
            cSource, &
            (/ 0_i8b, 0_i8b, 0_i8b, 0_i8b /), &
            (/ cSource%iDimT, cSource%iDimX, cSource%iDimY, 1_i8b /), &
            cModelParams.xyzsliceindex, cSource%iDimZ, .true.);
    end if

    ! 2) Transform source pulse in time
    call PrintToLog("Convert the source from distribution 0 to 1", 3)
    call ReorderDistr0ToDistr1(cSource%cGrid);
	call MapVt(cSource)
    call PrintToLog("Compute the Fourier Transform with respect to the T-axis", 3)
    !print *, size( cSource%cGrid%pacD1(:)) 
    !print *, "Punto di Interesse 2"

    ! 3) Create cSpace grid and fill it with source pulse delayed in z
    ! for each location on this processor:
    !   - get correct delay factor as a function of z
    !	- multiply the source signature with it and put this in the time trace
    call GridDistr1CreateEmpty(cSpace%cGrid);

    dDiffT = (cSpace%iStartT - cSource%iStartT)*cSpace%dDt
    
    do iLi = 0, cSpace%cGrid%iD0LocN-1

        !Z Position
        dZpos = (cSpace%cGrid%aiD0Loc(1+iLi,3) + cSpace%iStartZ)*cSpace%dDx
        do iOmega = 0, cSpace%cGrid%iD1TL-1

            dOmega = two_pi * iOmega * cSpace%dFnyq / cSpace%iDimT

            ! Account for shift in z and in t, and FFT correction factor
            dCorrection = exp(-im*dOmega*(dZpos-dDiffT))/real(2*cSpace%iDimT,dp)

            cSpace%cGrid%pacD1(1+iLi*cSpace%cGrid%iD1IS + iOmega*cSpace%cGrid%iD1TS) =&
                cSource%cGrid%pacD1(1 + iOmega*cSpace%cGrid%iD1TS) * dCorrection

        end do

    end do
    call DestructSpace(cSource)

    ! 4) Inverse transform in time by reverting to distr. 0
    call PrintToLog("Compute the inverse Fourier Transform with respect to the T-axis", 3)
    call TransformTInv(cSpace%cGrid)
    call MapVtInv(cSpace)
    call PrintToLog("Redistribute the result to distribution 0", 3)
    call ReorderDistr1ToDistr0(cSpace%cGrid);

    ! Deinitialize
    call SWStop(cswLinStep);

    END SUBROUTINE PlanewaveField
    
    SUBROUTINE PointSourceCloudField(cSpace)

    ! =============================================================================
    !
    !   Programmer: Agisilaos Matalliotakis // Date : 211108
    !
    !   Language: Fortran 90
    !
    !   Version Date    Comment
    !   ------- -----   -------
    !   1.0     090505  Original code (KH)
    !
    ! *****************************************************************************
    !
    !   DESCRIPTION
    !
    !   The subroutine PointSourceCloudField computes the primary field solution 
    !   generated from a cloud of point sources, having the same amplitude.
    !
    ! *****************************************************************************
    !
    !   INPUT/OUTPUT PARAMETERS
    !
    !   cSpace   io   type(space)  Space structure for which the primary field
    !                              solution is to be obtained
    !
    type(Space), intent(inout)::                cSpace;

    ! *****************************************************************************
    !
    !   LOCAL PARAMETERS
    !
    !   acTemp       char   Temporary char array for output log messages
    !
    character(LEN = 1024)::			acTemp;

    ! *****************************************************************************
    !
    !   I/O
    !
    !   log file entries
    ! =============================================================================
    call PrintToLog("PointSourceCloudField",1)
    call GridDistr0CreateEmpty(cSpace%cGrid);

    ! Test whether we have the right input, otherwise quit immediately
    if (cSpace%iSpaceIdentifier /= iSI_FIELD) then
        write (acTemp, '("Error occurred during the execution of PointSourceCloudField, aborting program")');
        call PrintToLog(acTemp, -1);
        write (acTemp, '("the space provided as input is not a FIELD.")');
        call PrintToLog(acTemp, -1);
        stop
    end if
    
    call SwStartAndCount(cswLinStep);
    
    call PointSourceCloudOperator(cSpace)
    cSpace%iSpaceIdentifier = iSI_CONTRASTSOURCE;
	call test_isnan(cSpace)
    call ContrastSourcetoField(cSpace, cSpace, .false.)
    
    ! Deinitialize
    call SWStop(cswLinStep);

    END SUBROUTINE PointSourceCloudField

    SUBROUTINE FieldtoContrastSource(cSpace,iContrastID,cInhomContrast)

    ! =============================================================================
    !
    !   Programmer: Koos Huijssen
    !
    !   Language: Fortran 90
    !
    !   Version Date    Comment
    !   ------- -----   -------
    !   1.0     090505  Original code (KH)
    !
    ! *****************************************************************************
    !
    !   DESCRIPTION
    !
    !   The subroutine FieldToContrastSource computes the contrast source for the
    !   given space - the type of the contrast source is determined by iContrastID.
    !
    ! *****************************************************************************
    !
    !   INPUT/OUTPUT PARAMETERS
    !
    !   cSpace          io  type(Space)   Space structure for which the contrast
    !                                     source is to be obtained
    !   iContrastID     i   i8b           Contrast Identification number
    !   cInhomContrast  i   type(Space)   Space containing the inhomogeneity
    !                                     contrast - if applicable
    !
    type(Space), intent(inout) ::	cSpace
    integer(i8b), intent(in) ::		iContrastID
    type(Space), intent(in) ::		cInhomContrast

    ! *****************************************************************************
    !
    !   LOCAL PARAMETERS
    !
    !   acTemp       char   Temporary char array for output log messages
    !
    character(LEN = 1024)::			acTemp;

    ! *****************************************************************************
    !
    !   I/O
    !
    !   log file entries
    !
    ! *****************************************************************************
    !
    !   SUBROUTINES/FUNCTIONS CALLED
    !
