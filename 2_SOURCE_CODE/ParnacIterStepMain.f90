    SUBROUTINE ZeroFirstPlane(cSpace)

    ! =============================================================================
    !
    !   Programmer: Koos Huijssen
    !
    !   Language: Fortran 90
    !
    !   Version Date    Comment
    !   ------- -----   -------
    !   1.0     090505  Original code (KH)
    !
    ! *****************************************************************************
    !
    !   DESCRIPTION
    !
    !   The subroutine ZeroFirstPlane puts the first plane in the Z-dimension in a
    !   Field space to zero. This is important if this field space is used as a
    !   contrast source to another space, and if at a different moment in the
    !   program the first plane of this space is be used as a pressure plane to
    !   account for the contrast sources at and before this plane. If we would not
    !   put it to zero in the contrast source to field computation, it would be
    !   accounted for twice.
    !
    ! *****************************************************************************
    !
    !   INPUT/OUTPUT PARAMETERS
    !
    !   cSpace   io   type(space)  Space in which the first plane in Z is put to
    !                              zero
    !
    type(Space), intent(inout)::            cSpace

    ! *****************************************************************************
    !
    !   LOCAL PARAMETERS
    !
    !   iErr            i8b    Error number
    !   iStart          i8b    Start index of the plane
    !   iEnd            i8b    End index of the plane
    !   iLi             i8b    Loop counter
    !   iLi2            i8b    Loop counter
    !   acTemp          char   Temporary char array for output log messages
    !
    integer(i8b)::                          iErr, iStart, iEnd
    integer(i8b)::                          iLi, iLi2
    character(len=1024)::                   acTemp

    ! *****************************************************************************
    !
    !   I/O
    !
    !   log file entries
    !
    ! *****************************************************************************
    !
    !   SUBROUTINES/FUNCTIONS CALLED
    !
    !   PrintToLog
    !
    ! =============================================================================

    call PrintToLog("ZeroFirstPlane",1)

    if (cSpace%cGrid%iDistr /= 0) then
        write (acTemp, '("Error occurred during the execution of ZeroFirstPlane, aborting program")');
        call PrintToLog(acTemp, -1);
        write (acTemp, '("the grid provided as input is not in distribution 0, but in distribution ", I3, "")') cSpace%cGrid%iDistr;
        call PrintToLog(acTemp, -1);
        stop
    end if

    !Check whether there are any positions in the first z-plane on this processor
    do iLi = 0, cSpace.cGrid.iD0LocN-1
        if (cSpace.cGrid.aiD0Loc(1+iLi, 3) == 0) then
            exit
        end if
    end do

    !If so, start from beginning position and load the last z-plane
    if(iLi < cSpace.cGrid.iD0LocN) then

        do iLi2 = iLi, cSpace.cGrid.iD0LocN-1
            if (cSpace.cGrid.aiD0Loc(1+iLi2, 3) /= 0) then
                exit
            end if
        end do

        iStart = iLi*cSpace%cGrid%iD0TL
        iEnd = iLi2*cSpace%cGrid%iD0TL-1

        do iLi = iStart, iEnd
            cSpace%cGrid%parD0(1+iLi) = 0.0_dp
        end do

    end if

    END SUBROUTINE ZeroFirstPlane

