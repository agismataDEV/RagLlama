    !
    real(8)    , intent(in):: D, E
    integer(i8b)    , intent(in)   :: F
    integer(i8b)     :: Vectordimensions,iLi
    type(Space) , intent(in)   :: B,C
    type(Space) , intent(inout)   :: A
