    SUBROUTINE UpdateRRMSerror(cRRMSNorm, cSpace, iBeam, iIteration)

    ! =============================================================================
    !
    !   Programmer: Koos Huijssen
    !
    !   Language: Fortran 90
    !
    !   Version Date    Comment
    !   ------- -----   -------
    !   1.0     090505  Original code (KH)
    !
    ! *****************************************************************************
    !
    !   DESCRIPTION
    !
    !   The subroutine UpdateRRMSerror updates the cRRMSNorm structure with the
    !   RRMS error of the current beam and iteration. This RRMS error is obtained
    !   as the Relative Root Mean Square error of the current iteration of the beam
    !   with respect to the previous iteration of the same beam. This is therefore
    !   more of a convergence check than a real error measure, and if it converges
    !   it is more the RRMS error of the PREVIOUS iteration than that of the
    !   current one. Moreover, it doesn't say that the scheme converges to the
    !   CORRECT solution. Only that it converges. If the RRMS error keeps on
    !   reducing, then we have convergence; if the RRMS error stays constant at
    !   a large value (say 1) or if it increases, then we have divergence.
    !   To reduce the storage between the iterationsw, only the RRMS error of the
    !   LAST plane of the field in the z-dimension is determined; it is assumed
    !   that it is a sufficient measure for the convergence of the rest.
    !   The subroutine also updates the arPreviousCorrection array to the last
    !   plane in z of the current iteration.
    !   In the subroutine, the data on all processors is collected by processor 0,
    !   which stores the total values for all beams and all iterations > 0.
    !
    ! *****************************************************************************
    !
    !   INPUT/OUTPUT PARAMETERS
    !
    !   cRRMSNorm   io   type(RRMSNormtype)   RRMSNorm structure to be updated
    !   cSpace      i    type(space)  Space for which the RRMSNorm is determined
    !   iBeam       i    i8b    Index number of the beam (closest to the source
    !                            is zero)
    !   iIteration  i    i8b    Iteration number (0 is primary field solution)
    !
    type(RRMSNormtype), INTENT(inout) :: cRRMSNorm
    type(Space), INTENT(in) :: cSpace
    integer(i8b), INTENT(in) :: iBeam, iIteration

    ! *****************************************************************************
    !
    !   LOCAL PARAMETERS
    !
    !   rRRMSNumDenom    dp   1D array of size 2 filled with 1) the sum of the
    !                         square errors of the positions on this processor, and
    !                         2) the sum of the square of the previous iteration of
    !                         the positions on this processor
    !   arRRMSNumDenom   dp   2D array used only by processor 0, which gathers all
    !                         numerator and denominator values of all processors
    !   prRRMSNumDenom   dp   pointer to an rRRMSNumDenom array
    !   parRRMSNumDenom  dp   pointer to an arRRMSNumDenom array
    !   iProcIndex       i4b  loop counter over the processor IDs
    !   iErr             i4b  error number to be used in MPI interfacing
    !   aiStatus         i4b  status of the MPI_Wait command
    !   aiReqSent        i4b  send request identification
    !   aiReqRecv        i4b  receive request identification
    !   acTemp     char   Temporary char array for output log messages
    !
    real(dp), target ::		rRRMSNumDenom(2)
    real(dp), target ::		arRRMSNumDenom(2,cSpace%cGrid%iProcN) !only used by processor 0
    real(dp), pointer ::	prRRMSNumDenom(:), parRRMSNumDenom(:,:)
    character(1024) ::		acTemp
    integer(i4b) ::	iProcIndex, iErr
    integer(i4b) ::	aiStatus(MPI_STATUS_SIZE)
    integer(i4b) ::	aiReqSent, aiReqRecv(cSpace%cGrid%iProcN-1);

    ! *****************************************************************************
    !
    !   I/O
    !
    !   log file entries
    !
    ! *****************************************************************************
    !
    !   SUBROUTINES/FUNCTIONS CALLED
    !
    !   PrintToLog
    !   MPI_ISend
    !   MPI_IRecv
    !   MPI_Wait
    !
    ! =============================================================================

    call PrintToLog("UpdateRRMSerror",1)

    !check whether cBeam is in distribution 0
    if (cSpace%cGrid%iDistr /= 0) then
        write (acTemp, '("Error occurred during the execution of UpdateRRMSerror, aborting program")');
        call PrintToLog(acTemp, -1);
        write (acTemp, '("the grid is not in distribution 0, but in distribution ", I3)') cSpace%cGrid%iDistr;
        call PrintToLog(acTemp, -1);
        stop
    end if

    !	calculate RRMS error for this processor
    !	calculate numerator and denominator separately
    rRRMSNumDenom = 0.0_dp
    if (cRRMSNorm%iStart.NE.-1) then
        rRRMSNumDenom(1) = sum((cSpace%cGrid%parD0(1+cRRMSNorm%iStart:cSpace.cGrid.iD0LocN*cSpace%cGrid%iD0TL) &
            - cRRMSNorm%arPreviouscorrection)**2)
        rRRMSNumDenom(2) = sum(cSpace%cGrid%parD0(1+cRRMSNorm%iStart:cSpace.cGrid.iD0LocN*cSpace%cGrid%iD0TL)**2)
    end if

    if (cSpace%cGrid%iProcID.NE.0) then
        ! communicate RRMS error to processor 0;
        ! communicate 0 if there is nothing on this processor

        prRRMSNumDenom => rRRMSNumDenom

        write (acTemp, '(" Send RRMS error")');	call PrintToLog(acTemp, 4);
        write (acTemp, '(" Num, Denom = ", E12.3, ", ",E12.3)') rRRMSNumDenom(1),rRRMSNumDenom(2);	call PrintToLog(acTemp, 4);

        call MPI_ISend( prRRMSNumDenom(1),  &
            2, &
            MPI_DOUBLE_PRECISION, &
            0,	&
            int(cSpace%cGrid%iProcID),	&
            MPI_COMM_WORLD, &
            aiReqSent, &
            iErr);

    else
        !	if ProcID=0, collect intermediate answers from all processors and calculate final RRMS error, write to LOGfile

        !First numerator/denominator couple is from Processor 0 itself
        arRRMSNumDenom(:,1) = rRRMSNumDenom
        parRRMSNumDenom => arRRMSNumDenom

        write (acTemp, '(" Receive RRMS errors")');	call PrintToLog(acTemp, 4);
        do iProcIndex = 1, cSpace%cGrid%iProcN-1
            call MPI_IRecv( parRRMSNumDenom(1,1+iProcIndex), &
                2, &
                MPI_DOUBLE_PRECISION, &
                int(iProcIndex), &
                int(iProcIndex), &
                MPI_COMM_WORLD, &
                aiReqRecv(iProcIndex), &
                iErr);
        end do

        write (acTemp, '(" Synchronize")');	call PrintToLog(acTemp, 4);
        ! We wait untill we have obtained and sent all the blocks
        do iProcIndex = 1, cSpace%cGrid%iProcN-1
            call MPI_Wait(aiReqRecv(iProcIndex), aiStatus, iErr);
            write (acTemp, '("ProcID= ,",I3,", Num, Denom = ", E12.3,", ",E12.3)') iProcIndex, arRRMSNumDenom(1,1+iProcIndex), arRRMSNumDenom(2,1+iProcIndex);
            call PrintToLog(acTemp, 5);
        end do

        cRRMSNorm%arRRMSNorm(1+iBeam,iIteration)=sqrt(sum(arRRMSNumDenom(1,:))/sum(arRRMSNumDenom(2,:)))

        write (acTemp,'("RRMSNorm: ", E14.5)') cRRMSNorm%arRRMSNorm(1+iBeam,iIteration)
        call PrintToLog(acTemp,1)
    end if

    !Update Previous Correction to the current correction
    if (cRRMSNorm%iStart.NE.-1) then
        cRRMSNorm%arPreviouscorrection=cSpace%cGrid%parD0(1+cRRMSNorm%iStart:cSpace.cGrid.iD0LocN*cSpace%cGrid%iD0TL)
    end if

    END SUBROUTINE UpdateRRMSerror

