    ! %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
    ! %
    ! %	CopyDataD2
    ! %
    ! %	Strips the margins in the T dimension used for the calculation of d/dz
    ! %
    ! %	NB
    ! %	- cGridSource and cGridDest should be in distribution 0
    ! %
    ! %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

    SUBROUTINE CopyDataD1(cGridSource, cGridDest, iTMargin)

    ! =============================================================================
    !
    !   Programmer: Koos Huijssen
    !
    !   Language: Fortran 90
    !
    !   Version Date    Comment
    !   ------- -----   -------
    !   1.0     090505  Original code (KH)
    !
    ! *****************************************************************************
    !
    !   DESCRIPTION
    !
    !   The subroutine CopyDataD1 copies the data from one grid to a grid with
    !   a smaller time dimension. This subroutine is used for stripping the margin
    !   in T that was necessary for the d/dz finite difference evaluation. It only
    !   works for grids in distribution 0. The margin which has to be removed at
    !   the beginning of each time trace needs to be given as input.
    !
    ! *****************************************************************************
    !
    !   INPUT/OUTPUT PARAMETERS
    !
    !   cGridSource   io   type(Grid)   Source grid
    !   cGridDest     io   type(Grid)   Destination grid
    !   iTMargin      i    i8b          Margin at the beginning of the T dimension
    !
    type(Grid), intent(inout)::                cGridSource, cGridDest;
    integer(i8b), intent(in)::                iTMargin;

    ! *****************************************************************************
    !
    !   LOCAL PARAMETERS
    !
    !   iT           i8b   loop counter over all time instants in an xyz-position
    !   iXYZ         i8b   loop counter over all xyz-positions in the beam
    !   iIndSource   i8b   index of the source data array
    !   iIndDest     i8b   index of the destination data array
    !
    integer(i8b)::                                iT, iXYZ, iIndSource, iIndDest


    ! *****************************************************************************
    !
    !   I/O
    !
    !   log file entries
    !
    ! *****************************************************************************
    !
    !   SUBROUTINES/FUNCTIONS CALLED
    !
    !   PrintToLog
    !
    ! =============================================================================

    call PrintToLog("CopyDataD1",4)

    ! Copy the contents of the trace without the T margin
    do iXYZ = 0, cGridDest.iD0LocN-1
        do iT = 0, cGridDest.iD0TL-1
            iIndSource	= 1 + iXYZ * cGridSource.iD0IS + (iT + iTMargin) * cGridSource.iD0TS;
            iIndDest	= 1 + iXYZ * cGridDest.iD0IS   + iT * cGridDest.iD0TS;

            cGridDest.parD0(iIndDest) = cGridSource.parD0(iIndSource);

        end do
    end do

    END SUBROUTINE CopyDataD1

    SUBROUTINE CopyDataD2(cGridSource, cGridDest, iXYZMargin)

    ! =============================================================================
    !
    !   Programmer: Koos Huijssen
    !
    !   Language: Fortran 90
    !
    !   Version Date    Comment
    !   ------- -----   -------
    !   1.0     090505  Original code (KH)
    !
    ! *****************************************************************************
    !
    !   DESCRIPTION
    !
    !   The subroutine CopyDataD2 copies the data from one grid to a grid with
    !   smaller dimensions in X,Y and Z. This subroutine is used for stripping the
    !   margins in X, Y and Z that were necessary for the d/dz finite difference
    !   evaluation. It only works for grids in distribution 2. The margin which has
    !   to be removed at the beginning the XYZ dimensions need to be given as
    !   input.
    !
    ! *****************************************************************************
    !
    !   INPUT/OUTPUT PARAMETERS
    !
    !   cGridSource   io   type(Grid)   Source grid
    !   cGridDest     io   type(Grid)   Destination grid
    !   iXYZMargin    i    i8b          Margin at the beginning of the XYZ
    !                                   dimensions
    !
    type(Grid), intent(inout)::                cGridSource, cGridDest;
    integer(i8b), intent(in)::                iXYZMargin(:);

    ! *****************************************************************************
    !
    !   LOCAL PARAMETERS
    !
    !   iT           i8b   loop counter over the t-axis in the beam
    !   iX           i8b   loop counter over the x-axis in the beam
    !   iY           i8b   loop counter over the y-axis in the beam
    !   iZ           i8b   loop counter over the z-axis in the beam
    !   iIndSource   i8b   index of the source data array
    !   iIndDest     i8b   index of the destination data array
    !
    integer(i8b)::                                iT, iX, iY, iZ, iIndSource, iIndDest

    ! *****************************************************************************
    !
    !   I/O
    !
    !   log file entries
    !
    ! *****************************************************************************
    !
    !   SUBROUTINES/FUNCTIONS CALLED
    !
    !   PrintToLog
    !
    ! =============================================================================

    call PrintToLog("CopyDataD2",4)

    ! Copy the contents of the slices without the XYZ margin
    do iT = 0, cGridDest.iD2LocN-1
        do iY = 0, cGridDest.iD2YL-1
            do iX = 0, cGridDest.iD2XL-1
                iIndSource = 1 + iT * cGridSource.iD2IS + (iX+iXYZMargin(1)) * cGridSource.iD2XS + (iY+iXYZMargin(2)) * cGridSource.iD2YS + iXYZMargin(3) * cGridSource.iD2ZS;
                iIndDest   = 1 + iT * cGridDest.iD2IS + iX * cGridDest.iD2XS   + iY * cGridDest.iD2YS;
                do iZ = 0, cGridDest.iD2ZL-1

                    cGridDest.pacD2(iIndDest) = cGridSource.pacD2(iIndSource);

                    iIndSource        = iIndSource + cGridSource.iD2ZS;
                    iIndDest        = iIndDest + cGridDest.iD2ZS;

                end do
            end do
        end do
    end do

    END SUBROUTINE CopyDataD2

    SUBROUTINE test_isnan(cSpace)

    ! =============================================================================
    !
    !   Programmer: Koos Huijssen
    !
    !   Language: Fortran 90
    !
    !   Version Date    Comment
    !   ------- -----   -------
    !   1.0     090505  Original code (KH)
    !
    ! *****************************************************************************
    !
    !   DESCRIPTION
    !
    !   The subroutine test_isnan tests whether there is a NaN or a very large
    !   number occurring in a space under test. This would be an indication of an
    !   error during calculation, and it would be a waste of resources to continue
    !   the evaluation.
    !
    ! *****************************************************************************
    !
    !   INPUT/OUTPUT PARAMETERS
    !
    !   cSpace   i   type(Space)   Space which needs to be tested
    !
    type(Space), INTENT(in) :: cSpace

    ! *****************************************************************************
    !
    !   LOCAL PARAMETERS
    !
    !   iLi   i8b   loop counter over all array elements
    !
    integer(i8b) :: iLi
    character(LEN=1024)::   acTemp;

    ! *****************************************************************************
    !
    !   I/O
    !
    !   log file entries
    !
    ! *****************************************************************************
    !
    !   SUBROUTINES/FUNCTIONS CALLED
    !
    !   PrintToLog
    !
    ! =============================================================================

    if(cSpace%cGrid%iDistr==0) then
            if (.not. ALL(cSpace%cGrid%ParD0<1E100_dp)) then
                !write (acTemp, '("Error: NaN or very large number encountered!!!" ,I3," - Processor checking in.")') cSpace.cGrid.iProcID; call PrintToLog(acTemp, 0);
                call PrintToLog("Error: NaN or very large number encountered!!!", 1)
                stop
            end if
    elseif(cSpace%cGrid%iDistr==1) then
        
            if (.not. ALL(real(cSpace%cGrid%PacD1,dp)<1E100_dp) .OR. .NOT.  ALL(dimag(cSpace%cGrid%PacD1)<1E100_dp) ) then
                call PrintToLog("Error: NaN or very large number encountered!!!",1)
                stop
            end if
    elseif(cSpace%cGrid%iDistr==2) then
            if (.not. ALL(real(cSpace%cGrid%PacD2,dp)<1E100_dp) .OR. .NOT.  ALL(dimag(cSpace%cGrid%PacD2)<1E100_dp)) then
                call PrintToLog("Error: NaN or very large number encountered!!!",1)
                stop
            end if
    end if

    END SUBROUTINE test_isnan

    SUBROUTINE TwoRealValueCalculation(a,B,C)
    ! =============================================================================
    !
    !   Programmer: Libertario Demi
    !
    !   Language: Fortran 90
    !
    !   Version Date    Comment
    !   ------- -----   -------
    !   1.0     210512  Original code (LD)
    !
    ! *****************************************************************************
    !
    ! *****************************************************************************
    !
    !   INPUT/OUTPUT PARAMETERS
    !
    !   cSpace             io   type(space)  Space to which the stored data array
    !                                        is subtracted.
    !   acStoredFieldName  io   char         Filename of the temp storage file.
    !
    type(Space), intent(in)   ::            B,C
    integer(i8b)::                          iLi, Vectordimensions
    character(len=1024)::                   acTemp;
    real(8), intent (inout):: a

    ! *****************************************************************************
    !
    !   I/O
    !
    !   log file entries and loading of the data array from temporary file
    !
    ! *****************************************************************************
    !
    !   SUBROUTINES/FUNCTIONS CALLED
    !
    !   SwStartAndCount
    !   PrintToLog
    !   int2str
    !   SWStop
    !
    ! =============================================================================

    a=0

    call PrintToLog("TwoRealCalculation",1)

    if (B%cGrid%iDistr /= 0) then
        write (acTemp, '("Error occurred during the execution of TwoRealCalculation, aborting program")');
        call PrintToLog(acTemp, -1);
        write (acTemp, '("the grid provided as input is not in distribution 0, but in distribution ", I3, "")') B%cGrid%iDistr;
        call PrintToLog(acTemp, -1);
        stop
    end if

    if (C%cGrid%iDistr /= 0) then
        write (acTemp, '("Error occurred during the execution of TwoRealCalculation, aborting program")');
        call PrintToLog(acTemp, -1);
        write (acTemp, '("the grid provided as input is not in distribution 0, but in distribution ", I3, "")') B%cGrid%iDistr;
        call PrintToLog(acTemp, -1);
        stop
    end if

    Vectordimensions=size(B%cGrid%parD0)

    do iLi = 0, Vectordimensions-1

        a = a+(B%cGrid%parD0(1+iLi)*C%cGrid%parD0(1+iLi))
    end do

    call PrintToLog("TwoRealCalculation Finished", 3)

    a=2*REAL(a);

    END SUBROUTINE TwoRealValueCalculation


