    SUBROUTINE CFPlaneSourcetoField(cSrcSpace,cDestSpace)

    ! =============================================================================
    !
    !   Programmer: Koos Huijssen
    !
    !   Language: Fortran 90
    !
    !   Version Date    Comment
    !   ------- -----   -------
    !   1.0     090505  Original code (KH)
    !
    ! *****************************************************************************
    !
    !   DESCRIPTION
    !
    !   The subroutine CFPlaneSourcetoField computes the field excited by a plane
    !   on which a pressure is defined in a domain behind the pressure plane.
    !   According to Huygens' principle, we would require a closed surface to
    !   compute the field from the (contrast) sources within the closed surface.
    !   Here, we only take a part of the XY-plane to compute the field of the
    !   sources in the shadow region of the surface.
    !
    ! *****************************************************************************
    !
    !   INPUT/OUTPUT PARAMETERS
    !
    !   cSrcSpace   io   type(space)  Space structure in which the pressure plane
    !                                 is stored. It is assumed to be stored on the
    !                                 first plane in z.
    !   cDestSpace  io   type(space)  Space structure for which the field
    !                                 solution is to be obtained
    !
    type(Space), intent(inout)::                cSrcSpace,cDestSpace;

    ! *****************************************************************************
    !
    !   LOCAL PARAMETERS
    !
    !   iLi             i8b    loop counter over all xyz-positions
    !   iStart          i8b    start of the xy-positions at z=0
    !   iEnd            i8b    end of the xy-positions at z=0
    !   iLT             i8b    loop counter over all t-instants
    !   iIndS           i8b    index for source array data
    !   iIndD           i8b    index for dest array data
    !   iMarginStartZ   i8b    Start margin in Z for d/dz operation
    !   iMarginStartT   i8b    Start margin in T for d/dz operation
    !   cSrcSpaceMargin type(Space)   Space for the field with margins for d/dz
    !   cSpaceMargin    type(Space)   Space for the field with margins for d/dz
    !   cSpaceXYZMargin type(Space)   Space for the field with margins in XYZ only
    !   aiMargin        i8b    Margins in all four dimensions T/X/Y/Z
    !   dTempPhaseDelay dp     Maximum phase delay in the source
    !   acTemp          char   Temporary char array for output log messages
    !
    integer(i8b)::              iLi, iStart, iEnd, iLT, iIndS, iIndD, iMarginStartZ, iMarginStartT;
    integer(i8b) ::             iErr
    integer(i4b) ::				iMPIErr
    type(Space)::               cSrcSpaceMargin, cSpaceMargin,cSpaceXYZMargin
    integer(i8b) ::             aiMargin(4)
    real(dp) ::					dTempPhaseDelay
    character(len=1024)::       acTemp

    ! *****************************************************************************
    !
    !   I/O
    !
    !   log file entries
    !
    ! *****************************************************************************
    !
    !   SUBROUTINES/FUNCTIONS CALLED
    !
    !   SwStartAndCount
    !   PrintToLog
    !   iBeamOffsetT
    !   iBeamOffsetX
    !   iBeamOffsetY
    !   InitSpace
    !   InitGrid
    !   GridDistr0CreateEmpty
    !   GridDistr0DeAllocate
    !   ConvolutionGreen
    !   DestructSpace
    !   BeamDerivativeZ
    !   CopyDataD1
    !   CopyDataD2
    !   ReorderDistr0ToDistr1
    !   ReorderDistr1ToDistr2
    !   ReorderDistr2ToDistr1
    !   ReorderDistr1ToDistr0
    !   SWStop
    !
    ! *****************************************************************************
    !
    !   PSEUDOCODE
    !
    !   Compute the margins in all directions needed for the d/dz operator
    !   Initialize a field space including possible margins
    !   Initialize a source space including the margin in time
    !   Copy the input pressure plane in to the source space
    !   Compute the convolution of the source with the Green's function to obtain
    !     the field in the field space
    !   Destruct the source space
    !   Apply the d/dz operator to the field space
    !   Reduce the field space with margins to a field space without margins
    !
    ! =============================================================================

    call SwStartAndCount(cswLinStep);
    call PrintToLog("CFPlaneSourcetoField",1)

    !The Correction Field source slice is a pressure slice and therefore
    ! acts as a force source. We need to apply the spatial derivative in z
    ! and therefore need a margin.

    ! 1) Compute the necessary margin (in all directions) to compute the linear step
    aiMargin(1) = ceiling(abs(cModelParams%FDXOrder/2 *cDestSpace%dTanT) - 1e-5)
    aiMargin(2) = ceiling(abs(cModelParams%FDXOrder/2 *cDestSpace%dTanX) - 1e-5)
    aiMargin(3) = ceiling(abs(cModelParams%FDXOrder/2 *cDestSpace%dTanY) - 1e-5)
    aiMargin(4) = cModelParams%FDXOrder/2;

    !		write(acTemp, '("aiMargin in T, X, Y, Z: ", 4I2)') aiMargin
    !		call PrintToLog(acTemp,2)

    !New global start positions in Z and T
    iMarginStartZ = cDestSpace%iStartZ-aiMargin(4)
    iMarginStartT = cDestSpace%iStartT+iBeamOffsetT(cDestSpace,-aiMargin(4))-aiMargin(1)

    !Determine source dimensions: determined by either the dim+margin of the field or the minimum size
    ! required for the source+margin

    ! 2) Create a space (cSpaceMarg) with the extra margin in all txyz-directions
    call InitSpace(cSpaceMargin, iSI_FIELD, cModelParams.UseYSymmetry, &
        cDestSpace%iDimT+2*aiMargin(1),cDestSpace%iDimX+2*aiMargin(2), cDestSpace%iDimY+2*aiMargin(3), cDestSpace%iDimZ+2*aiMargin(4), &
        iMarginStartT,0_i8b,0_i8b,iMarginStartZ, 0_i8b,0_i8b,0_i8b, &
        cDestSpace%dFnyq, cDestSpace%dTanX, cDestSpace%dTanY, cDestSpace%dTanT)
    call InitGrid(cSpaceMargin, cDestSpace%cGrid%iProcN, cDestSpace%cGrid%iProcID, (/ .true., .true., .true., .true./));

    ! Correct the iXYTMargins, may be changed because of resetting array size in InitGrid
    ! NB This only gives the correct value for the Start margin, which may or may not be the
    ! same as the End margin...
    ! (however, the start margin is the only one that is needed in CopyDataD1/2 anyway...)
    ! the correction needs to be done for the X and Y dimensions only, for the T and Z dimensions,
    ! the array size resetting only gives extra points at the end, start margin is unaffected
    aiMargin(2) = cDestSpace%iStartX - (cSpaceMargin%iStartX + iBeamOffsetX(cSpaceMargin,aiMargin(4)));
    aiMargin(3) = cDestSpace%iStartY - (cSpaceMargin%iStartY + iBeamOffsetY(cSpaceMargin,aiMargin(4)));

    !		call PrintToLog("Variables in cSpaceMargin",-1)
    !		call SpaceInfo(cSpaceMargin)

    write(acTemp, '("aiMargin in T, X, Y, Z: ", 4I2)') aiMargin
    call PrintToLog(acTemp,2)

    !Create cSrcSpaceMargin to include margin in time dimension
    ! and copy the field slice into it
    ! then deallocate grid in cSrcSpace
    call InitSpace(cSrcSpaceMargin, iSI_FIELDSLICE, cModelParams.UseYSymmetry, &
        cSpaceMargin%iDimT,cSrcSpace%iDimX, cSrcSpace%iDimY, cSrcSpace%iDimZ, &
        cSrcSpace%iStartT-aiMargin(1), cSrcSpace%iStartX, cSrcSpace%iStartY, cSrcSpace%iStartZ, 0_i8b,0_i8b,0_i8b, &
        cSrcSpace%dFnyq, cSrcSpace%dTanX, cSrcSpace%dTanY, cSrcSpace%dTanT)
    call InitGrid(cSrcSpaceMargin, cSrcSpace%cGrid%iProcN, cSrcSpace%cGrid%iProcID, (/ .false., .false., .false., .false./));
    call GridDistr0CreateEmpty(cSrcSpaceMargin%cGrid)

    !call PrintToLog("Variables in cSrcSpaceMargin",-1)
    !call SpaceInfo(cSrcSpaceMargin)

    ! Check whether there are any positions in the FIRST z-plane on this processor
    ! if so, obtain first position of z=0-plane on this processor
    do iStart = 0, cSrcSpace.cGrid.iD0LocN-1
        if (cSrcSpace.cGrid.aiD0Loc(1+iStart, 3) == 0) then
            exit
        end if
    end do

    !If there are traces on this processor, copy the traces into
    ! cSrcSpaceMargin (watch the margin in T!!!) and immediately multiply
    ! the pressure with the Fsource correction factor
    ! Fsource = -2*Ptempsource: minus since source term should be - d/dz F(x,t)
    if(iStart<cSrcSpace.cGrid.iD0LocN) then

        !Obtain last position of z=0 plane on this processor
        ! This implementation assumes that the index over the z-dimension
        ! is the slowest one compared to the indices over x and y
        do iEnd = iStart, cSrcSpace.cGrid.iD0LocN-1
            if (cSrcSpace.cGrid.aiD0Loc(1+iEnd, 3) /= 0) then
                exit
            end if
        end do

        do iLi = iStart, iEnd-1
            do iLT = 0,cSrcSpace%cGrid%iD0TL-1

                iIndS= 1 + iLi * cSrcSpace%cGrid%iD0TL + iLT * cSrcSpace%cGrid%iD1TS
                iIndD= 1 + iLi * cSrcSpaceMargin%cGrid%iD0TL + iLT * cSrcSpaceMargin%cGrid%iD1TS + aiMargin(1)
                cSrcSpaceMargin%cGrid%parD0(iIndD) = -2.0_dp * cSrcSpace%cGrid%parD0(iIndS);

            end do
        end do

    end if
    call GridDistr0DeAllocate(cSrcSpace%cGrid)

    ! 3) Compute the convolution on cSpaceMargin
    call PrintToLog('Compute the convolution in the Correction Field plane step',2);
    call ConvolutionGreen(cSrcSpaceMargin, cSpaceMargin, .false.);
    call DestructSpace(cSrcSpaceMargin)

    !call ExportSlice(trim(sOutputDir) // trim(sOutputRoot) // "cSpaceMargin",&
    !	"cSpaceMargin",cSpaceMargin, &
    !	(/ 0_i8b, 0_i8b, 0_i8b, 0_i8b /), &
    !	(/ cSpaceMargin%iDimT, cSpaceMargin%iDimX, 1_i8b, cSpaceMargin%iDimZ /), &
    !	.true.);

    ! 4) Compute the Finite Difference operator on cSpaceMarg
    call PrintToLog('Compute the Finite Difference in z', 2);
    call BeamDerivativeZ(cSpaceMargin)

    ! 5a) Strip the d/dz margins in T
    call PrintToLog('Store the results in a beam without margins', 2);
    call InitSpace(cSpaceXYZMargin, iSI_FIELD, cModelParams.UseYSymmetry, &
        cDestSpace%iDimT,cSpaceMargin%iDimX, cSpaceMargin%iDimY, cSpaceMargin%iDimZ, &
        iMarginStartT+aiMargin(1),0_i8b,0_i8b,iMarginStartZ,0_i8b,0_i8b,0_i8b, &
        cDestSpace%dFnyq, cDestSpace%dTanX, cDestSpace%dTanY, cDestSpace%dTanT)
    call InitGrid(cSpaceXYZMargin, cDestSpace%cGrid%iProcN, cDestSpace%cGrid%iProcID, (/ .false., .false., .false., .false./));
    call GridDistr0CreateEmpty(cSpaceXYZMargin.cGrid);
    call CopyDataD1(cSpaceMargin%cGrid, cSpaceXYZMargin.cGrid, aiMargin(1));
    call DestructSpace(cSpaceMargin);

    ! 5b) Strip the d/dz margins in XYZ
    !   - Reorder to distribution 2
    call ReorderDistr0ToDistr1(cSpaceXYZMargin%cGrid);
    call ReorderDistr1ToDistr2(cSpaceXYZMargin%cGrid);
    call GridDistr2CreateEmpty(cDestSpace%cGrid);
    call CopyDataD2(cSpaceXYZMargin%cGrid, cDestSpace%cGrid, aiMargin(2:4));

    !   - revert to distribution 0
    call DestructSpace(cSpaceXYZMargin);
    call ReorderDistr2ToDistr1(cDestSpace%cGrid);
    call ReorderDistr1ToDistr0(cDestSpace%cGrid);

    cDestSpace%iSpaceIdentifier=iSI_FIELD;

    ! Deinitialize
    call SWStop(cswLinStep);

    END SUBROUTINE CFPlaneSourcetoField

