    SUBROUTINE StorePlane(cSpace,acStoredFieldName)

    ! =============================================================================
    !
    !   Programmer: Koos Huijssen
    !
    !   Language: Fortran 90
    !
    !   Version Date    Comment
    !   ------- -----   -------
    !   1.0     090505  Original code (KH)
    !
    ! *****************************************************************************
    !
    !   DESCRIPTION
    !
    !   The subroutine StorePlane stores the first plane in Z of the field in a
    !   given space to a temporary file in the temp dir. Only a Distr. 0 data array
    !   is stored, it is assumed that in a later read all other information is
    !   known. The storing is done in a blockwise fashion.
    !
    ! *****************************************************************************
    !
    !   INPUT/OUTPUT PARAMETERS
    !
    !   cSpace             io   type(space)  Space from which the data at the plane
    !                                        needs to be stored.
    !   acStoredFieldName  io   char         Filename of the temp storage file.
    !
    type(Space), intent(inout)::            cSpace
    character(*), intent(in):: acStoredFieldName

    ! *****************************************************************************
    !
    !   LOCAL PARAMETERS
    !
    !   iErr            i8b    Error number
    !   iStart          i8b    Start index of the plane
    !   iLength         i8b    Length of the data array of the plane
    !   iLi             i8b    Loop counter
    !   iLi2            i8b    Loop counter
    !   iLast           i8b    Index of the last full block
    !   iBlockL         i8b    Size of the blocks
    !   acFilename      char   Total filename including path and suffix
    !   acTemp          char   Temporary char array for output log messages
    !
    integer(i8b)::                          iErr, iStart, iLength
    integer(i8b)::                          iLi, iLi2, iLast;
    integer(i8b), parameter::               iBlockL                = 2**48;
    character(len=1024)::                   acFileName
    character(len=1024)::                   acTemp;

    ! *****************************************************************************
    !
    !   I/O
    !
    !   log file entries and storing of the data array to temporary file
    !
    ! *****************************************************************************
    !
    !   SUBROUTINES/FUNCTIONS CALLED
    !
    !   SwStartAndCount
    !   PrintToLog
    !   int2str
    !   SWStop
    !
    ! =============================================================================

    call PrintToLog("StoreField",1)

    if (cSpace%cGrid%iDistr /= 0) then
        write (acTemp, '("Error occurred during the execution of StorePlane, aborting program")');
        call PrintToLog(acTemp, -1);
        write (acTemp, '("the grid provided as input is not in distribution 0, but in distribution ", I3, "")') cSpace%cGrid%iDistr;
        call PrintToLog(acTemp, -1);
        stop
    end if

    call SwStartAndCount(cswDisk)

    !Check whether there are any positions in the FIRST z-plane on this processor
    !	!Check whether there are any positions in the last z-plane on this processor
    do iLi = 0, cSpace.cGrid.iD0LocN-1
        !		if (cSpace.cGrid.aiD0Loc(1+iLi, 3) == cSpace.iDimZ-1) then
        if (cSpace.cGrid.aiD0Loc(1+iLi, 3) == 0) then
            exit
        end if
    end do

    !If so, start from beginning position and save the FIRST z-plane
    !	!If so, start from beginning position and save the last z-plane
    if(iLi<cSpace.cGrid.iD0LocN) then

        do iLi2 = iLi, cSpace.cGrid.iD0LocN-1
            if (cSpace.cGrid.aiD0Loc(1+iLi2, 3) /= 0) then
                exit
            end if
        end do

        iStart = iLi*cSpace%cGrid%iD0TL
        iLength = (iLi2-iLi)*cSpace%cGrid%iD0TL
        !		iLength = (cSpace.cGrid.iD0LocN-iLi)*cSpace%cGrid%iD0TL
        !			!iLength assumes that from iStart, the rest of the space belongs to the last slice...

        ! Write field to disk
        call PrintToLog('Write P_lin  to disk', 2);
        call SwStartandCount(cswDiskAcces)
        acFileName        = trim(sOutputDir)//acStoredFieldName//int2str(cSpace%cGrid%iProcID);
        open (unit=iExportUNIT,file=trim(acFileName),status="REPLACE",form="UNFORMATTED",iostat=iErr)
        if(iErr/=0) then
            write(acTemp,"('Error in StorePlane, file ',A20,'does not exist')") acStoredFieldName
            call PrintToLog(acTemp,-1)
            stop
        end if
        iLast = int(iLength/iBlockL);
        do iLi = 0, iLast-1
            write (unit=iExportUNIT, iostat=iErr) cSpace%cGrid%parD0(1+iStart+iLi*iBlockL:iStart+(iLi+1)*iBlockL);
        end do
        !		write (unit=iExportUNIT, iostat=iErr) cSpace%cGrid%parD0(1+iStart+iLast*iBlockL:cSpace%cGrid%iD0LocSize);
        write (unit=iExportUNIT, iostat=iErr) cSpace%cGrid%parD0(1+iStart+iLast*iBlockL:iStart+iLength);
        close (unit=iExportUNIT);
        call SwStop(cswDiskAcces)

    end if

    call SWStop(cswDisk)

    END SUBROUTINE StorePlane

