    SUBROUTINE SourceDimensions(iDimT, iDimX, iDimY, dDt, dDx)

! =============================================================================
!
!   Programmer: Koos Huijssen
!
!   Language: Fortran 90
!
!   Version Date    Comment
!   ------- -----   -------
!   1.0     090505  Original code (KH)
!
! *****************************************************************************
!
!   DESCRIPTION
!
!   The subroutine SourceDimensions returns the sizes in number of points for
!   each of the source signature and aperture types in the three dimensions
!   t,x and y. The data are extracted from the cSourceParams structure.
!
! *****************************************************************************
!
!   INPUT/OUTPUT PARAMETERS
!
!   iDimT   o  i8b  Size of the T-dimension
!   iDimX   o  i8b  Size of the X-dimension
!   iDimY   o  i8b  Size of the Y-dimension
!   dDt     i  dp   Stepsize in T
!   dDx     i  dp   Stepsize in X
!
        real(dp), intent(in) :: dDt, dDx
        integer(i8b), intent(out) :: iDimT, iDimX, iDimY

! *****************************************************************************
!
!   LOCAL PARAMETERS
!
!   i        i8b   temporary variable
!   dims     i8b   size of the dimensions
!   dTdelay  dp    maximal time delay in the data file source description
!   acTemp   char  temporary char array for output log messages
!
        character(LEN=2048) ::                acTemp; 
        integer(i8b) :: i, dims(3)
        real(dp) :: dTdelay

! *****************************************************************************
!
!   I/O
!
!   if applicable, checking the dimensions of the source in the source
!   data file.
!
! *****************************************************************************
!
!   SUBROUTINES/FUNCTIONS CALLED
!
!   BINARY_read3Darraysize
!   BINARY_read3DarrayTdelay
!   ASCII_read3Darraysize
!   BINARY_read1Darraysize
!   ASCII_read1Darraysize
!
! =============================================================================

        if (trim(cSourceParams%srcfilename) /= 'none') then
            ! If we have a data file source description, then check the sizes from
            ! data file
!    print *, "CCCCCCCCCCCCCCCCCCCCCCCCCCCC"
#ifndef RUNESTIMATOR
            i = index(cSourceParams%srcfilename, '.')
            if (cSourceParams%srcfilename(i + 1:i + 3) == 'bin') then
                dims = BINARY_read3Darraysize(trim(sInputDir)//trim(cSourceParams%srcfilename))
                dTdelay = BINARY_read3DarrayTdelay(trim(sInputDir)//trim(cSourceParams%srcfilename))
                !dTdelay is given in milliseconds, normalize
                dTdelay = dTdelay*cModelParams.freq0*1.0e-6
            else
                dims = ASCII_read3Darraysize(trim(sInputDir)//trim(cSourceParams%srcfilename))
            end if

            iDimT = dims(1) + ceiling(dTdelay/dDt)
            iDimX = dims(2)
            iDimY = dims(3)
!                print *, "AAAAAAAAAAAAAAAAAAAAAAAA"
#else
!                iDimT = 0
!                iDimX = 0
!                iDimY = 0
!                print *, "BBBBBBBBBBBBBBBBBBBBBBBB"
#endif

        else
            ! For parametric source descriptions, there are specific rules for the
            ! source sizes. Each time, we also include an extra margin for possible
            ! filtering effects

            if (cSourceParams%srcsigntype == iGI_GAUSSIAN) then
                iDimT = ceiling((cSourceParams%Tpulse + cSourceParams%maxphasedelay)/dDt)
            else if (cSourceParams%srcsigntype == iGI_BLACKMAN) then
                iDimT = ceiling((1.1_dp*cSourceParams%Tpulse + cSourceParams%maxphasedelay)/dDt)
            else
#ifndef RUNESTIMATOR
                i = index(cSourceParams%srcsignfilename, '.')
                if (cSourceParams%srcsignfilename(i + 1:i + 3) == 'bin') then
                    iDimT = BINARY_read1Darraysize(trim(sInputDir)//trim(cSourceParams%srcsignfilename)) + ceiling(real(cSourceParams%maxphasedelay)/dDt)
                else
                    iDimT = ASCII_read1Darraysize(trim(sInputDir)//trim(cSourceParams%srcsignfilename)) + ceiling(real(cSourceParams%maxphasedelay)/dDt)
                end if
#else
!                iDimT = 0
#endif
            end if

            if (cSourceParams%srcshapetype == iHI_CYLINDRICAL) then
                ! take 10 points extra in each dimension to allow for Gibb's
                iDimX = 2*((cSourceParams%radius/dDx) + 10) + 1
                iDimY = 2*((cSourceParams%radius/dDx) + 10) + 1
            else if (cSourceParams%srcshapetype == iHI_RECTANGULAR) then
                ! take 10 points extra in each dimension to allow for Gibb's
                iDimX = 2*(cSourceParams%rectwidth/(2.0_dp*dDx) + 10) + 1
                iDimY = 2*(cSourceParams%rectheight/(2.0_dp*dDx) + 10) + 1
            else if (cSourceParams%srcshapetype == iHI_TRIANGULAR) then
                ! take 10 points extra in each dimension to allow for Gibb's
                iDimX = 2*(sqrt(3.0_dp)*cSourceParams%triangedge/(4.0_dp*dDx) + abs(cSourceParams%triangxcenter)/dDx + 10) + 1
                iDimY = 2*(cSourceParams%triangedge/(2.0_dp*dDx) + 10) + 1
            else if (cSourceParams%srcshapetype == iHI_POINTSOURCE) then
                iDimX = 1
                iDimY = 1
            else if (cSourceParams%srcshapetype == iHI_PHASEDARRAY) then
                ! take 15 points extra in each dimension to allow for Gibb's
                iDimX = 2*(ceiling(cSourceParams%arraywidth/(2.0_dp*dDx)) + 15) + 1
                iDimY = 2*(ceiling(cSourceParams%elheight/(2.0_dp*dDx)) + 15) + 1
!KH                else if (cSourceParams%srcshapetype==iHI_MATRIXARRAY) then
!KH                        ! take 15 points extra in each dimension to allow for Gibb's
!KH                        iDimX=2*(ceiling((cSourceParams%arraywidth)/(2.0_dp*dDx))+15)+1
!KH                        iDimY=2*(ceiling(cSourceParams%elheight/(2.0_dp*dDx))+15)+1
            end if

        end if

    END SUBROUTINE SourceDimensions

