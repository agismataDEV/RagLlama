    SUBROUTINE InitSource(cSource)

! =============================================================================
!
!   Programmer: Koos Huijssen
!
!   Language: Fortran 90
!
!   Version Date    Comment
!   ------- -----   -------
!   1.0     090505  Original code (KH)
!
! *****************************************************************************
!
!   DESCRIPTION
!
!   The subroutine InitSource fills the Source space from a 3D array generated
!   by InitSourceExcitation. The generated source is either given by a data
!   file or by a parametric source description in the config file.
!
! *****************************************************************************
!
!   INPUT/OUTPUT PARAMETERS
!
!   cSource   io   type(space)  source space to be filled - it is assumed that
!                               it is already initialized and that a Grid 0
!                               array is allocated
!
        type(Space), intent(inout)::                        cSource; 
! *****************************************************************************
!
!   LOCAL PARAMETERS
!
!   iXInd     i8b    beam index in X
!   iYind     i8b    beam index in Y
!   iLi       i8b    loop counter over the positions in the beam
!   iLt       i8b    loop counter over the time instants in the beam
!   Ksource   i8b    size of the source in points in the T-dimension
!   Lsource   i8b    size of the source in points in the X-dimension
!   Msource   i8b    size of the source in points in the Y-dimension
!   acTemp    char   temporary char array for output log messages
!   error     i8b    error number
!   Excitation  dp   3D array containing the source description in (t,x,y)
!
        integer(i8b)::                        iXInd, iYInd, iLi, iLt
        integer(i8b) ::                        Ksource, LSource, MSource
        character(len=1024) ::        acTemp; 
        integer(i8b) ::                        error
        real(dp), allocatable :: Excitation(:, :, :)

! *****************************************************************************
!
!   I/O
!
!   log file entries and, if applicable, reading of the source data files
!
! *****************************************************************************
!
!   SUBROUTINES/FUNCTIONS CALLED
!
!   alloctest
!   PrintToLog
!   SourceDimensions
!   InitSourceExcitation
!
! =============================================================================

        write (acTemp, '("InitSource")'); call PrintToLog(acTemp, 1); 
        if (cSource.iSpaceIdentifier /= iSI_PRIMARYSOURCE) then
            call PrintToLog("Error. Cannot create a source in a non-source space", -1)
            stop; 
        end if

        if (cSource.cGrid.iDistr /= 0) then
            call PrintToLog("Error. Distribution of Source should be 0", -1)
            stop; 
        end if

        !Get and test dimensions

        call SourceDimensions(Ksource, Lsource, Msource, cSource%dDt, cSource%dDx)
        if ((cSource%iDimT < Ksource) .or. (cSource%iDimX < Lsource) .or. (cSource%iDimY < Msource)) then
            write (acTemp, "('Error in InitSource, source space dimensions are too small for the source')")
            call PrintToLog(acTemp, -1)
            write (acTemp, "('Source dims TXY',I6,I6,I6,', Source Space dims TXY',I6,I6,I6)") &
                Ksource, Lsource, Msource, cSource%iDimT, cSource%iDimX, cSource%iDimY
            call PrintToLog(acTemp, -1)
            stop
        end if

        !Allocate the Excitation array
        allocate (Excitation(1:cSource%iDimT, 1:cSource%iDimX, 1:cSource%iDimY), STAT=error)
        call alloctest(error, 'Excitation in InitSource')

        ! Fill the Excitation with the source description
        call InitSourceExcitation(Excitation, cSource)

        ! Store Excitation in the first z-plane in cSource
        !         note that Excitation should always be centered around x=0,y=0, i.e. the zero position
        !        is found at position floor(size(Excitation,[2 or 3])/2)+1
        !        In t Excitation starts at t =0 and it is assumed below that the source is located in the plane z=0
        do iLi = 0, cSource%cGrid%iD0LocN - 1
            !only on the first plane in the grid (whatever iStartZ may be...)
            if (cSource%cGrid%aiD0Loc(1 + iLi, 3) == 0) then

                !Position in the global grid, assuming that the XY slice is at Z=0.
                iXInd = cSource%cGrid%aiD0Loc(1 + iLi, 1) + cSource%iStartX
                iYInd = cSource%cGrid%aiD0Loc(1 + iLi, 2) + cSource%iStartY

                if ((abs(iXInd) < size(Excitation, 2)/2) .AND. (abs(iYInd) < size(Excitation, 3)/2)) then

                    !Put the non-zero values from the source into the grid
                    !- The time dimension in Excitation may be longer than the grid allows.
                    !- We assume here that iStartT<=0.
                    !- The index in Excitation always starts at one, translate this to the beam index.
                    do iLt = 0, min(cSource%cGrid%iD0TL + cSource%iStartT, int(size(Excitation, 1), i8b)) - 1
                        cSource%cGrid%parD0(1 + iLi*cSource%cGrid%iD0IS + (iLt - cSource%iStartT)*cSource%cGrid%iD0TS) = &
                            Excitation(1 + iLt, iXInd + size(Excitation, 2)/2 + 1, iYInd + size(Excitation, 3)/2 + 1); 
                    end do

                end if
            end if
        end do

        ! Deallocate the Excitation array
        if (allocated(Excitation)) then
            deallocate (Excitation); 
        end if
    END SUBROUTINE InitSource

    SUBROUTINE InitSource_Planewave(cSource)

! =============================================================================
!
!   Programmer: Koos Huijssen
!
!   Language: Fortran 90
!
!   Version Date    Comment
!   ------- -----   -------
!   1.0     090505  Original code (KH)
!
! *****************************************************************************
!
!   DESCRIPTION
!
!   The subroutine InitSource_Planewave fills the Source space for the case of
!   the plane wave source description. We 'trick' the InitSourceExcitation
!   by making it believe that it creates a point source. For this the function
!   pre-assumes that the source shape type is set to iHI_POINTSOURCE. In the
!   subroutine, the point source description is multiplied with a normalization
!   factor, and it is stored into the Source space, which is assumed to have
!   a size in X and Y of 1 point.
!
! *****************************************************************************
!
!   INPUT/OUTPUT PARAMETERS
!
!   cSource   io   type(space)  source space to be filled - it is assumed that
!                               it is already initialized and that a Grid 0
!                               array is allocated
!
        type(Space), intent(inout)::                        cSource; 
! *****************************************************************************
!
!   LOCAL PARAMETERS
!
!   iLt       i8b    loop counter over the time instants in the beam
!   Ksource   i8b    size of the source in points in the T-dimension
!   Lsource   i8b    size of the source in points in the X-dimension
!   Msource   i8b    size of the source in points in the Y-dimension
!   acTemp    char   temporary char array for output log messages
!   error     i8b    error number
!   Excitation  dp   3D array containing the source description in (t,x,y)
!
        integer(i8b)::                        iLt
        integer(i8b) ::                Ksource, LSource, MSource
        character(len=1024) ::        acTemp
        integer(i8b) ::                        error
        real(dp), allocatable :: Excitation(:, :, :)

! *****************************************************************************
!
!   I/O
!
!   log file entries and, if applicable, reading of the source data files
!
! *****************************************************************************
!
!   SUBROUTINES/FUNCTIONS CALLED
!
!   alloctest
!   PrintToLog
!   SourceDimensions
!   InitSourceExcitation
!
! =============================================================================

        write (acTemp, '("InitSource")'); call PrintToLog(acTemp, 1); 
        if (cSource.iSpaceIdentifier /= iSI_PRIMARYSOURCE) then
            call PrintToLog("Error. Cannot create a source in a non-source space", -1)
            stop; 
        end if

        if (cSource.cGrid.iDistr /= 0) then
            call PrintToLog("Error. Distribution of Source should be 0", -1)
            stop; 
        end if

        !Get and test dimensions
        call SourceDimensions(Ksource, Lsource, Msource, cSource%dDt, cSource%dDx)
        if ((cSource%iDimT < Ksource) .or. (cSource%iDimX < Lsource) .or. (cSource%iDimY < Msource)) then
            write (acTemp, "('Error in InitSource_Planewave, source space dimensions are too small for the source')")
            call PrintToLog(acTemp, -1)
            write (acTemp, "('Source dims TXY',I6,I6,I6,', Source Space dims TXY',I6,I6,I6)") &
                Ksource, Lsource, Msource, cSource%iDimT, cSource%iDimX, cSource%iDimY
            call PrintToLog(acTemp, -1)
            stop
        end if

        allocate (Excitation(1:cSource%iDimT, 1:cSource%iDimX, 1:cSource%iDimY), STAT=error)
        call alloctest(error, 'Excitation in InitSource_Planewave')

        ! This functions allocates and fills Excitation
        call InitSourceExcitation(Excitation, cSource)

        ! We had made InitSourceExcitation think that it created a point source
        ! Now we correct for the extra factor included due to the point source description
        Excitation = Excitation/((cModelParams%freq0/cMediumParams%c0)*(1.0_dp/cSource.dDx)**2)

        ! Store Excitation in the first position in cSource on each processor
        !         note that Excitation should always be centered around x=0,y=0. It starts at t =0 and z=0 for all values
        do iLt = 0, min(cSource%cGrid%iD0TL, int(size(Excitation, 1), i8b)) - 1
            cSource%cGrid%parD0(1 + iLt*cSource%cGrid%iD0TS) = &
                Excitation(1 + iLt, 1, 1); 
        end do

        ! Deallocate the array allocated in init_source_2
        if (allocated(Excitation)) then
            deallocate (Excitation); 
        end if

    END SUBROUTINE InitSource_Planewave

